const fs = require('fs');
const path = require('path');
const root = path.resolve(__dirname, '..');
const src = fs.readFileSync(path.join(root, 'src/config/constants.ts'), 'utf8');
const m = src.match(/ALLOWED_UPLOAD_EXTENSIONS\s*=\s*new Set\(\[([\s\S]*?)\]\)/);
if (!m) {
  console.error('ALLOWED_UPLOAD_EXTENSIONS not found');
  process.exit(1);
}
const exts = [...m[1].matchAll(/'\.([a-z0-9]+)'/g)].map((x) => '.' + x[1]);
if (!exts.length) {
  console.error('No extensions parsed');
  process.exit(1);
}
const outPath = path.join(root, 'public/admin/allowed-extensions.js');
const content = `// AUTO-GENERATED by scripts/generate-allowed-extensions.cjs — do not edit.
// Source: src/config/constants.ts ALLOWED_UPLOAD_EXTENSIONS
export const CHAT_ALLOWED_EXTENSIONS = new Set(${JSON.stringify(exts, null, 2)});

export const CHAT_FILE_ACCEPT = [...CHAT_ALLOWED_EXTENSIONS].join(',');
`;
fs.writeFileSync(outPath, content);
console.log(`Wrote ${outPath} (${exts.length} extensions)`);
