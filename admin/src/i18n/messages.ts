function detectLocale() {
  try {
    if (typeof localStorage !== 'undefined') {
      const saved = localStorage.getItem(LANG_KEY);
      if (saved === 'en' || saved === 'zh-Hant') return saved;
    }
  } catch {
    /* node / private mode */
  }
  try {
    const nav = (
      (typeof navigator !== 'undefined' &&
        (navigator.language || navigator.userLanguage)) ||
      'en'
    ).toLowerCase();
    if (nav.startsWith('zh')) return 'zh-Hant';
  } catch {
    /* ignore */
  }
  return 'en';
}

function safeStorageSet(key, value) {
  try {
    if (typeof localStorage !== 'undefined') localStorage.setItem(key, value);
  } catch {
    /* ignore */
  }
}

let locale = detectLocale();

export function getLocale() {
  return locale;
}

export function setLocale(next) {
  if (next !== 'en' && next !== 'zh-Hant') return;
  locale = next;
  safeStorageSet(LANG_KEY, next);
}

export function t(path) {
  const parts = path.split('.');
  let cur = dict[locale] || dict.en;
  for (const p of parts) {
    if (cur && typeof cur === 'object' && p in cur) cur = cur[p];
    else {
      cur = dict.en;
      for (const q of parts) {
        if (cur && typeof cur === 'object' && q in cur) cur = cur[q];
        else return path;
      }
      break;
    }
  }
  return typeof cur === 'string' ? cur : path;
}

/** True if path resolves to a real string (not missing key). */
export function hasT(path) {
  return t(path) !== path;
}

/** t() with {name} placeholders: tf('keys.ipCount', { n: 3 }) */
export function tf(path, vars = {}) {
  let s = t(path);
  for (const [k, v] of Object.entries(vars)) {
    s = s.replaceAll(`{${k}}`, String(v));
  }
  return s;
}

export function langSwitchHtml() {
  const aria = locale === 'zh-Hant' ? '語言' : 'Language';
  return `
  <div class="lang-switch" role="group" aria-label="${aria}">
    <button type="button" data-lang="en" class="${locale === 'en' ? 'is-active' : ''}">EN</button>
    <button type="button" data-lang="zh-Hant" class="${locale === 'zh-Hant' ? 'is-active' : ''}">中文</button>
  </div>`;
}
